package grade;

public class GradeBean {
	private int kor, eng, math;
	private String name;

	public void setKor() {
		this.kor = kor;
	}

	public void setEng(int eng) {
		this.eng = eng;
	}

	public void setMath(int math) {
		this.math = math;
	}
	public void setName(String name){
		this.name = name;
	}
	public int getKor() {
		return kor;
	}

	public int getEng() {
		return eng;
	}

	public int getMath() {
		return math;
	}
	public String getName(){
		return name;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
}
